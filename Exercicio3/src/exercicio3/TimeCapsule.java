package exercicio3;

public class TimeCapsule {
	//Attributes
	private int hour;
	private int minute;
	
	//Methods
	TimeCapsule(){}
	
	public void setHour(int x){
		hour = x;
	}
	public void setMinute(int x){
		minute = x;
	}
	public int getHour(){return hour;}
	public int getMinute(){return minute;}
}